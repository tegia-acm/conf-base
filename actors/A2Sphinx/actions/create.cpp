std::string A2Sphinx::create(const std::shared_ptr<message_t> &message, const nlohmann::json &route_params)
{
	int _STATUS_ = 200;
	////////////////////////////////////////////////////////////////////////////////////////

	std::string index_name = route_params["index_name"].get<std::string>();
	std::string command = this->paths.bin + "/indexer " + index_name + " --config " + this->paths.config + "/_all_index.conf";
	
	int ret = system( command.c_str() );

	////////////////////////////////////////////////////////////////////////////////////////
	return core::cast<std::string>(_STATUS_);
};